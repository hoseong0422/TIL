// 자바는 대소문자를 구분하므로 대소문자에 유의하자!

public class HelloJava { //HelloJava 클래스의 범위 시작

    // main 메소드 (프로그램의 시작점)
    public static void main(String[] args) { //main() 메서드 범위 시작
        System.out.println("Hello Java!"); // 값을 콘솔에 출력, 자바는 문자열을 ""로 감싸주어여 한다
    } //main() 메서드 범위 끝

} //HelloJava 클래스의 범위 끝
